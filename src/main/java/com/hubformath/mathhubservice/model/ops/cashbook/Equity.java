package com.hubformath.mathhubservice.model.ops.cashbook;

import com.fasterxml.jackson.annotation.JsonIgnore;
import com.hubformath.mathhubservice.model.auth.User;
import com.hubformath.mathhubservice.model.systemconfig.EquityType;
import com.hubformath.mathhubservice.model.systemconfig.PaymentMethod;
import jakarta.persistence.Column;
import jakarta.persistence.Entity;
import jakarta.persistence.GeneratedValue;
import jakarta.persistence.GenerationType;
import jakarta.persistence.Id;
import jakarta.persistence.JoinColumn;
import jakarta.persistence.ManyToOne;
import jakarta.persistence.Table;
import org.hibernate.annotations.CreationTimestamp;
import org.hibernate.annotations.UpdateTimestamp;

import java.time.LocalDateTime;
import java.util.Objects;

@Entity
@Table(name = "equities")
@SuppressWarnings("DefaultAnnotationParam") // Columns generated by @JoinColumn are unique by default
public class Equity {
    @Id
    @GeneratedValue(strategy = GenerationType.UUID)
    private String id;

    @ManyToOne
    @JoinColumn(name = "payment_method_id", unique = false, nullable = false)
    private PaymentMethod paymentMethod;

    @Column(name = "narration")
    private String narration;

    @ManyToOne
    @JoinColumn(name = "equity_type_id", unique = false, nullable = false)
    private EquityType type;

    @Column(name = "amount")
    private Double amount;

    @ManyToOne
    @JoinColumn(name = "created_by", unique = false, nullable = false)
    private User createdBy;

    @CreationTimestamp
    @Column(name = "created_at")
    @JsonIgnore
    private LocalDateTime createdAt;

    @UpdateTimestamp
    @Column(name = "updated_at")
    @JsonIgnore
    private LocalDateTime updatedAt;

    @SuppressWarnings("unused")
    public Equity() {
        // Required by JPA
    }

    public Equity(PaymentMethod paymentMethod, String narration, EquityType type, Double amount, User createdBy) {
        this.paymentMethod = paymentMethod;
        this.narration = narration;
        this.type = type;
        this.amount = amount;
        this.createdBy = createdBy;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public PaymentMethod getPaymentMethod() {
        return paymentMethod;
    }

    public void setPaymentMethod(PaymentMethod paymentMethod) {
        this.paymentMethod = paymentMethod;
    }

    public String getNarration() {
        return narration;
    }

    public void setNarration(String narration) {
        this.narration = narration;
    }

    public EquityType getType() {
        return type;
    }

    public void setType(EquityType type) {
        this.type = type;
    }

    public Double getAmount() {
        return amount;
    }

    public void setAmount(Double amount) {
        this.amount = amount;
    }

    public User getCreatedBy() {
        return createdBy;
    }

    public void setCreatedBy(User createdBy) {
        this.createdBy = createdBy;
    }

    public LocalDateTime getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(LocalDateTime createdAt) {
        this.createdAt = createdAt;
    }

    public LocalDateTime getUpdatedAt() {
        return updatedAt;
    }

    public void setUpdatedAt(LocalDateTime updatedAt) {
        this.updatedAt = updatedAt;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (!(o instanceof Equity equity)) return false;
        return Objects.equals(getId(), equity.getId())
                && Objects.equals(getPaymentMethod(), equity.getPaymentMethod())
                && Objects.equals(getNarration(), equity.getNarration())
                && Objects.equals(getType(), equity.getType())
                && Objects.equals(getAmount(), equity.getAmount())
                && Objects.equals(getCreatedBy(), equity.getCreatedBy());
    }

    @Override
    public int hashCode() {
        return Objects.hash(getId(),
                            getPaymentMethod(),
                            getNarration(),
                            getType(),
                            getAmount(),
                            getCreatedBy());
    }

    @Override
    public String toString() {
        return "Equity{" +
                "id='" + id + '\'' +
                ", paymentMethod=" + paymentMethod +
                ", narration='" + narration + '\'' +
                ", type=" + type +
                ", amount=" + amount +
                ", createdBy=" + createdBy +
                '}';
    }
}
